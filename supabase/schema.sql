ALTER TABLE documents
ADD CONSTRAINT documents_custom_type_rules_check
	CHECK (
		(
			type = 'other'
			AND custom_type IS NOT NULL
			AND LENGTH(BTRIM(custom_type)) > 0
			AND LENGTH(BTRIM(custom_type)) <= 60
		)
		OR (type <> 'other' AND custom_type IS NULL)
	);

