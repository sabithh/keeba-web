CREATE TABLE IF NOT EXISTS vault_items (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
	encrypted_payload TEXT NOT NULL CHECK (LENGTH(BTRIM(encrypted_payload)) > 0),
	iv TEXT NOT NULL CHECK (LENGTH(BTRIM(iv)) > 0),
	salt TEXT NOT NULL CHECK (LENGTH(BTRIM(salt)) > 0),
	kdf_algorithm TEXT NOT NULL DEFAULT 'pbkdf2-sha256',
	kdf_iterations INTEGER NOT NULL DEFAULT 600000 CHECK (kdf_iterations BETWEEN 200000 AND 2000000),
	key_version SMALLINT NOT NULL DEFAULT 1 CHECK (key_version >= 1),
	created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
	updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_vault_items_user_created_at
	ON vault_items (user_id, created_at DESC);

CREATE OR REPLACE FUNCTION update_profiles_timestamp()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
	NEW.updated_at = NOW();
	RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_profiles_updated_at ON profiles;
DROP TRIGGER IF EXISTS trg_vault_items_updated_at ON vault_items;

CREATE TRIGGER trg_profiles_updated_at
BEFORE UPDATE ON profiles
FOR EACH ROW
EXECUTE FUNCTION update_profiles_timestamp();

CREATE TRIGGER trg_vault_items_updated_at
BEFORE UPDATE ON vault_items
FOR EACH ROW
EXECUTE FUNCTION update_profiles_timestamp();

