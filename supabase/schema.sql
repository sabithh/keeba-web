UPDATE documents
SET custom_type = 'other'
WHERE type = 'other'
	AND (custom_type IS NULL OR LENGTH(BTRIM(custom_type)) = 0);

UPDATE documents
SET custom_type = LEFT(BTRIM(custom_type), 60)
WHERE type = 'other'
	AND custom_type IS NOT NULL;

UPDATE documents
SET custom_type = NULL
WHERE type <> 'other';

