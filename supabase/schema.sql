ALTER TABLE chat_messages
ADD COLUMN IF NOT EXISTS thread_id UUID;

DO $$
BEGIN
	IF NOT EXISTS (
		SELECT 1
		FROM pg_constraint
		WHERE conname = 'chat_messages_thread_id_fkey'
	) THEN
		ALTER TABLE chat_messages
		ADD CONSTRAINT chat_messages_thread_id_fkey
			FOREIGN KEY (thread_id)
			REFERENCES chat_threads(id)
			ON DELETE CASCADE;
	END IF;
END $$;

WITH users_with_legacy AS (
	SELECT DISTINCT user_id
	FROM chat_messages
	WHERE thread_id IS NULL
),
created_threads AS (
	INSERT INTO chat_threads (user_id, title, created_at, last_message_at)
	SELECT
		u.user_id,
		'Imported chat',
		COALESCE((
			SELECT MIN(m.created_at)
			FROM chat_messages m
			WHERE m.user_id = u.user_id AND m.thread_id IS NULL
		), NOW()),
		COALESCE((
			SELECT MAX(m.created_at)
			FROM chat_messages m
			WHERE m.user_id = u.user_id AND m.thread_id IS NULL
		), NOW())
	FROM users_with_legacy u
	RETURNING id, user_id
)
UPDATE chat_messages m
SET thread_id = t.id
FROM created_threads t
WHERE m.user_id = t.user_id
	AND m.thread_id IS NULL;

UPDATE chat_threads t
SET last_message_at = latest.last_message_at
FROM (
	SELECT thread_id, MAX(created_at) AS last_message_at
	FROM chat_messages
	WHERE thread_id IS NOT NULL
	GROUP BY thread_id
) latest
WHERE t.id = latest.thread_id;

DO $$
BEGIN
	IF NOT EXISTS (SELECT 1 FROM chat_messages WHERE thread_id IS NULL) THEN
		ALTER TABLE chat_messages ALTER COLUMN thread_id SET NOT NULL;
	END IF;
END $$;

